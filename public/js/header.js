$(function(){
    $.ajax({
      url:"header.html",
      type:"get",
      success:function(res){
        //动态添加<link>到<head>元素中，自动引入header.css
        $("<link rel='stylesheet' href='./css/header.css'>").appendTo("head");
        //用接收到的片段代替页面上空的<header>
        $(res)//<header>
        .replaceAll("#header");
        $(".header_top_nav>ul>li").hover(function(){
            $(this).children(":nth-child(2)").toggle();
        })
        .children(":nth-child(2)").hide();  
        $(".header_top_logo_center>input").focus(function(){
          $(this).css("outline","none")
        })   
      }
    })
  })